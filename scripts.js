$('#validationForm').submit(function(event) {
    event.preventDefault();
    let errorFound = false;

    if (!isValidEmailAddress($('#email').val())) {
        $('#emailError').html('Incorrect Email');
        errorFound = true;
    } else {
        $('#emailError').html('');
    }

    if (!$.isNumeric($('#telephone').val())) {
        $('#telephoneError').html('Incorrect Phone (numbers only)');
        errorFound = true;
    } else {
        $('#telephoneError').html('');
    }

    if ($('#password').val() != $('#confirm').val() || $('#confirm').val() == '') {
        $('#confirmError').html('Incorrect Password');
        errorFound = true;
    } else {
        $('#confirmError').html('');
    }

    if (errorFound) {
        alert('Issues found, please correct the following issues before proceeding');
    } else {
        location.reload();
        alert('Success!');
    }

});